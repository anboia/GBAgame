#include "gba_input.h"





// ========================================================
// ===== GLOBALS ==========================================


u16 key_curr = 0;
u16 key_prev = 0;



// ========================================================
// ===== FUNCTIONS ========================================



void key_poll(void)
{
	key_prev = key_curr;
	key_curr = ~(REG_KEY) & KEY_MASK;
}
